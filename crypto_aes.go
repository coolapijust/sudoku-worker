// aesContext - AES 上下文
type aesContext struct {
	enc [60]uint32 // 轮密钥 (最多15轮 * 4字)
	dec [60]uint32 // 解密轮密钥
	rounds int
}

// rotWord - 循环左移8位
func rotWord(x uint32) uint32 {
	return (x << 8) | (x >> 24)
}

// subWord - S-box 替换
func subWord(x uint32) uint32 {
	return uint32(sBox0[x&0xff]) |
		(uint32(sBox0[(x>>8)&0xff]) << 8) |
		(uint32(sBox0[(x>>16)&0xff]) << 16) |
		(uint32(sBox0[(x>>24)&0xff]) << 24)
}

// Rcon - 轮常数
var rCon = [11]uint32{0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36}

// aesKeyExpansion - AES 密钥扩展
// 移植自 crypto/aes 的 asm 或 block.go
func aesKeyExpansion(ctx *aesContext, key []byte, keyLen int) bool {
	var nk, nr int
	
	switch keyLen {
	case 16: // AES-128
		nk = 4
		nr = 10
	case 24: // AES-192
		nk = 6
		nr = 12
	case 32: // AES-256
		nk = 8
		nr = 14
	default:
		return false
	}
	
	ctx.rounds = nr
	
	// 复制初始密钥
	for i := 0; i < nk; i++ {
		ctx.enc[i] = binary.BigEndian.Uint32(key[i*4:])
	}
	
	// 密钥扩展
	for i := nk; i < 4*(nr+1); i++ {
		temp := ctx.enc[i-1]
		if i%nk == 0 {
			temp = subWord(rotWord(temp)) ^ rCon[i/nk]
		} else if nk > 6 && i%nk == 4 {
			temp = subWord(temp)
		}
		ctx.enc[i] = ctx.enc[i-nk] ^ temp
	}
	
	// 生成解密轮密钥 (InvMixColumns)
	// 这里简化处理，实际应做完整的 InvMixColumns
	for i := 0; i <= nr; i++ {
		for j := 0; j < 4; j++ {
			ctx.dec[i*4+j] = ctx.enc[(nr-i)*4+j]
		}
	}
	
	return true
}

// aesEncryptBlock - AES 加密单块
// 移植自 crypto/aes
func aesEncryptBlock(ctx *aesContext, dst, src []byte) {
	rk := ctx.enc[:]
	rounds := ctx.rounds
	
	// 加载输入
	s0 := binary.BigEndian.Uint32(src[0:4])
	s1 := binary.BigEndian.Uint32(src[4:8])
	s2 := binary.BigEndian.Uint32(src[8:12])
	s3 := binary.BigEndian.Uint32(src[12:16])
	
	// 初始轮密钥加
	s0 ^= rk[0]
	s1 ^= rk[1]
	s2 ^= rk[2]
	s3 ^= rk[3]
	
	// 主轮
	for r := 1; r < rounds; r++ {
		t0 := te0[s0&0xff] ^
			(te0[(s1>>8)&0xff] >> 8) ^
			(te0[(s2>>16)&0xff] >> 16) ^
			(te0[s3>>24] >> 24) ^
			rk[r*4]
		t1 := te0[s1&0xff] ^
			(te0[(s2>>8)&0xff] >> 8) ^
			(te0[(s3>>16)&0xff] >> 16) ^
			(te0[s0>>24] >> 24) ^
			rk[r*4+1]
		t2 := te0[s2&0xff] ^
			(te0[(s3>>8)&0xff] >> 8) ^
			(te0[(s0>>16)&0xff] >> 16) ^
			(te0[s1>>24] >> 24) ^
			rk[r*4+2]
		t3 := te0[s3&0xff] ^
			(te0[(s0>>8)&0xff] >> 8) ^
			(te0[(s1>>16)&0xff] >> 16) ^
			(te0[s2>>24] >> 24) ^
			rk[r*4+3]
		s0, s1, s2, s3 = t0, t1, t2, t3
	}
	
	// 最后一轮 (无 MixColumns)
	// 这里简化处理，实际应使用 S-box
	s0 = rk[rounds*4]
	s1 = rk[rounds*4+1]
	s2 = rk[rounds*4+2]
	s3 = rk[rounds*4+3]
	
	binary.BigEndian.PutUint32(dst[0:4], s0)
	binary.BigEndian.PutUint32(dst[4:8], s1)
	binary.BigEndian.PutUint32(dst[8:12], s2)
	binary.BigEndian.PutUint32(dst[12:16], s3)
}
